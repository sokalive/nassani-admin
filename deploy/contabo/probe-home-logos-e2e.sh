IDS=()
IMGS=()
for i in 1 2 3 4; do
  IMG=/tmp/home-logo-$TS-$i.jpg
  make_jpg "$IMG" >/dev/null
  B64=$(base64 -w0 "$IMG")
  RESP=$(curl -fsS -X POST "$API/api/home-logos" \
    -H "X-Admin-Token: $TOK" -H 'Content-Type: application/json' \
    -d "{\"title\":\"$TAG $i\",\"subtitle\":\"sub $i\",\"image\":\"data:image/jpeg;base64,$B64\",\"active\":true,\"sortOrder\":$i}")
  ID=$(python3 -c "import json,sys; print(json.loads(sys.argv[1])['id'])" "$RESP")
  URL=$(python3 -c "import json,sys; d=json.loads(sys.argv[1]); print(d.get('imageUrl') or d.get('image') or '')" "$RESP")
  echo "CREATED id=$ID url=$URL"
  curl -fsS -o /dev/null -w "http=%{http_code}\n" "$URL"
  BASE=$(basename "$(python3 -c "from urllib.parse import urlparse; print(urlparse('$URL').path)")")
  test -f "$UP/$BASE" || { echo "MISSING_FILE $UP/$BASE"; exit 1; }
  echo "FILE_OK $UP/$BASE"
  IDS+=("$ID")
  IMGS+=("$URL")
done

# Edit first logo
EDIT_IMG=/tmp/home-logo-$TS-edit.jpg
make_jpg "$EDIT_IMG" >/dev/null
B64E=$(base64 -w0 "$EDIT_IMG")
EDIT=$(curl -fsS -X PUT "$API/api/home-logos/${IDS[0]}" \
  -H "X-Admin-Token: $TOK" -H 'Content-Type: application/json' \
  -d "{\"title\":\"$TAG 1 Updated\",\"subtitle\":\"edited\",\"image\":\"data:image/jpeg;base64,$B64E\",\"active\":true,\"sortOrder\":1}")
EDIT_URL=$(python3 -c "import json,sys; d=json.loads(sys.argv[1]); print(d.get('imageUrl') or d.get('image') or '')" "$EDIT")
echo "EDITED ${IDS[0]} -> $EDIT_URL"
curl -fsS -o /dev/null -w "edit_http=%{http_code}\n" "$EDIT_URL"

# Reorder (reverse)
ORDERS=$(python3 <<PY
import json
ids = list(map(int, "${IDS[*]}".split()))
print(json.dumps({"orders": [{"id": i, "sortOrder": n} for n, i in enumerate(reversed(ids))]}))
PY
)
curl -fsS -X POST "$API/api/home-logos/reorder" \
  -H "X-Admin-Token: $TOK" -H 'Content-Type: application/json' \
  -d "$ORDERS" >/dev/null
echo "REORDERED"

# Public API
curl -fsS "$API/api/home-logos" > /tmp/home-logos-public.json
python3 -c "
import json
d=json.load(open('/tmp/home-logos-public.json'))
assert isinstance(d,list)
found=[x for x in d if '$TAG' in str(x.get('title',''))]
assert len(found)>=4, found
print('PUBLIC_COUNT', len(found))
for x in found:
  assert x.get('imageUrl') or x.get('image'), x
print('PUBLIC_OK')
"

# DB check
psql "$DATABASE_URL" -tAc "SELECT count(*) FROM home_logos WHERE title LIKE '$TAG%';" | awk '{print "DB_COUNT="$1}'

# Disable one
curl -fsS -X PUT "$API/api/home-logos/${IDS[1]}" \
  -H "X-Admin-Token: $TOK" -H 'Content-Type: application/json' \
  -d "{\"title\":\"$TAG 2\",\"subtitle\":\"\",\"image\":\"${IMGS[1]}\",\"active\":false,\"sortOrder\":2}" >/dev/null
curl -fsS "$API/api/home-logos" > /tmp/home-logos-public2.json
python3 -c "
import json
d=json.load(open('/tmp/home-logos-public2.json'))
found=[x for x in d if x.get('id')==${IDS[1]}]
assert not found, 'disabled logo still public'
print('DISABLE_OK')
"

# Delete all
for id in "${IDS[@]}"; do
  curl -fsS -X DELETE "$API/api/home-logos/$id" -H "X-Admin-Token: $TOK"
done
psql "$DATABASE_URL" -tAc "SELECT count(*) FROM home_logos WHERE title LIKE '$TAG%';" | awk '{print "DB_AFTER_DELETE="$1}'
curl -fsS "$API/api/home-logos" > /tmp/home-logos-after.json
python3 -c "
import json
d=json.load(open('/tmp/home-logos-after.json'))
assert not any('$TAG' in str(x.get('title','')) for x in d)
print('APP_REMOVED_OK')
"
